package br.pb.com.programming.basic;

public class Exercise04 {

	/*
	 * 4. Write a Java program to print the result of the following operations.
	 */
	public static void main(String[] args) {
		int operationA = (-5 + 8 * 6);
		int operationB = ((55 + 9) % 9);
		int operationC = (20 + -3 * 5 / 8);
		int operationD = (5 + 15 / 3 * 2 - 8 % 3);

		System.out.println(operationA);
		System.out.println(operationB);
		System.out.println(operationC);
		System.out.println(operationD);
	}
}